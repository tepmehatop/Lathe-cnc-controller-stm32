// ============================================================
// Serial Monitor команды ELS — Этап 9
//
// Вызывать ELS_Serial_Process() из loop().
// Формат команд: ASCII строка + Enter (\n или \r\n).
//
// Команды:
//   HELP              — список команд
//   INFO              — текущее состояние
//   START             — начать движение
//   STOP              — остановить
//   MODE:N            — режим (0=FEED, 1=AFEED, 2=THREAD, 3=CONE)
//   FEED:N            — подача мм/об × 100 (5..200)
//   AFEED:N           — асинхронная подача мм/мин × 100 (5..300)
//   PITCH:N           — шаг резьбы, 0.001 мм (100..5000)
//   STARTS:N          — число заходов резьбы (1..8)
//   ZERO:Y или ZERO:X — обнулить позицию оси
//   LIMIT:OFF         — отключить лимиты
//   LIMIT:ON          — включить лимиты
//   55                — тестовый пресет (RPM 300, FEED 0.20мм/об)
// ============================================================

void ELS_Serial_Init(void);
void ELS_Serial_Process(void);

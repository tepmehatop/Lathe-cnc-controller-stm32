/**
 * @file wifi_config.h
 * @brief WiFi настройки для ELS Display (ESP32-S3)
 *
 * =====================================================================
 * РЕЖИМ 1 — Подключение к домашнему роутеру (STA)
 *   Заполните WIFI_STA_SSID и WIFI_STA_PASS.
 *   ESP32 подключится к вашей сети. IP-адрес для входа в веб-интерфейс
 *   будет показан в НАСТРОЙКАХ дисплея (двойной тап на ⚠ в statusbar).
 *
 * РЕЖИМ 2 — Прямое подключение без роутера (AP)
 *   Оставьте WIFI_STA_SSID пустым ("").
 *   ESP32 создаст свою WiFi сеть, подключитесь к ней и зайдите:
 *     http://192.168.4.1
 *
 * =====================================================================
 * ! ВНИМАНИЕ: не коммитьте этот файл с реальными паролями в git !
 * =====================================================================
 */

#ifndef WIFI_CONFIG_H
#define WIFI_CONFIG_H

// === РЕЖИМ 1: Ваш домашний роутер ========================================
// Заполните, если хотите подключить ESP32 к существующей WiFi сети.
// Оставьте пустыми "" для прямого подключения (режим AP).

#define WIFI_STA_SSID  ""        // Например: "MyHomeWiFi"
#define WIFI_STA_PASS  ""        // Например: "mypassword123"

// === РЕЖИМ 2: Прямое подключение (AP) =====================================
// Параметры точки доступа, которую создаёт ESP32 когда WIFI_STA_SSID пустой.
// Или при таймауте подключения к роутеру (12 секунд).

#define WIFI_AP_SSID   "ELS-WiFi"    // Имя сети ESP32
#define WIFI_AP_PASS   "12345678"    // Пароль (минимум 8 символов)

// Таймаут подключения к STA (мс). После — переход в AP режим.
#define WIFI_STA_TIMEOUT_MS  12000

#endif // WIFI_CONFIG_H
